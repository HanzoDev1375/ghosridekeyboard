package rkr.simplekeyboard.inputmethod.logger;
import android.util.Log;

public class Logger {
  
  
  public static void e(String str,String strs){
    Log.e(str,str);
  }
  public static void w(String str,String strs){
    Log.w(str,str);
  }
  public static void d(String str,String strs){
    Log.d(str,str);
  }
  
}
